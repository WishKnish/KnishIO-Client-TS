import Subscribe from './Subscribe'
import { gql } from '@urql/core'
import type { GraphQLClient as IGraphQLClient } from '@/types'

/**
 * WalletStatusSubscribe handles real-time wallet status events
 */
export default class WalletStatusSubscribe extends Subscribe {
  constructor(graphQLClient: IGraphQLClient) {
    super(graphQLClient)
    this.$__subscribe = gql`
      subscription onWalletStatus($bundle: String!, $token: String!) {
        WalletStatus(bundle: $bundle, token: $token) {
          bundle
          token
          admission
          balance
        }
      }
    `
  }
}